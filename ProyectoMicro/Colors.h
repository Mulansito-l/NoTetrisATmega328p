#define magenta	0xF81F	// RB
#define gray	0x0821	// 00001 000001 00001

#define COLOR_I_PIECE    0x05FF  // I Piece (Cyan/Light Blue)
#define COLOR_J_PIECE    0x001F  // J Piece (Blue)
#define COLOR_L_PIECE    0xFA00  // L Piece (Orange)
#define COLOR_O_PIECE    0xFFE0  // O Piece (Yellow)
#define COLOR_S_PIECE    0x07E0  // S Piece (Green)
#define COLOR_Z_PIECE    0xF800  // Z Piece (Red)
#define COLOR_T_PIECE    0xF81F  // T Piece (Magenta)

#endif /* COLORS_H_ */